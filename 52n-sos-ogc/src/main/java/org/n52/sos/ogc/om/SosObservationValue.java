package org.n52.sos.ogc.om;

/**
 * represents a value of an observation
 * 
 * @author Christoph Stasch
 * 
 */
public class SosObservationValue {

    /** value of the observation */
    private String value;

    /** type of the observation value */
    private String valueType;

    /** unit of the observation value */
    private String unit;

    /**
     * standard constructor
     * 
     * @param valuep
     *        value of the observation
     * @param valueTypep
     *        type of the observation value
     * @param unitp
     *        unit of the observation value
     */
    public SosObservationValue(String valuep, String valueTypep, String unitp) {
        this.value = valuep;
        this.valueType = valueTypep;
        this.unit = unitp;
    }

    /**
     * @return Returns the unit.
     */
    public String getUnit() {
        return unit;
    }

    /**
     * @return Returns the value.
     */
    public String getValue() {
        return value;
    }

    /**
     * @return Returns the valueType.
     */
    public String getValueType() {
        return valueType;
    }

    /**
     * @param unit
     *        The unit to set.
     */
    public void setUnit(String unit) {
        this.unit = unit;
    }

    /**
     * @param value
     *        The value to set.
     */
    public void setValue(String value) {
        this.value = value;
    }

    /**
     * @param valueType
     *        The valueType to set.
     */
    public void setValueType(String valueType) {
        this.valueType = valueType;
    }

}
